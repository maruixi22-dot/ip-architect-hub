/**
 * 生成标题库的提示词
 */
export function generateTitleTemplatesPrompt(context: AIPromptContext): string {
  return `你是一位爆款标题创作专家，深谙各平台标题技巧。

## 任务说明
为 IP 的每个内容支柱生成 10 个高质量标题。

## 内容支柱信息
\`\`\`json
${JSON.stringify(context.contentPillars, null, 2)}
\`\`\`

## 目标受众信息
\`\`\`json
${JSON.stringify(context.targetAudience, null, 2)}
\`\`\`

## 标题创作技巧

### 标题类型（每个支柱至少包含2种类型）

1. **痛点型**：直击用户痛点
   - 示例："25岁后才懂的5个职场真相"

2. **数字型**：用数字增强可信度
   - 示例："3个技巧让你效率提升10倍"

3. **反常识型**：挑战固有认知
   - 示例："为什么你越努力越穷？"

4. **情绪型**：激发情绪共鸣
   - 示例："这一刻，我突然不想努力了"

5. **故事型**：用故事吸引阅读
   - 示例："从月薪5k到年入百万，我只做对了这件事"

6. **警告型**：制造紧迫感
   - 示例："停止做这5件事，你会越来越好看"

7. **承诺型**：给予明确承诺
   - 示例："30天养成这个习惯，改变你的人生"

8. **对比型**：通过对比突出价值
   - 示例："普通女孩vs优质女孩，差距就在这3点"

### 标题优化原则
1. **具体化**：避免抽象，用具体案例
2. **数字化**：使用数字增加说服力
3. **情绪化**：激发情绪反应
4. **简洁化**：控制在20字以内
5. **差异化**：避免同质化表达

### 平台特色
- **小红书**：emoji + 关键词 + 痛点/好处 + 数字
  - 示例："😭25岁后才懂的职场真相...建议收藏"
- **抖音**：悬念 + 情绪 + 利益点 + 行动号召
  - 示例："你还在这样上班吗？快停下！看视频教你"

## 输出格式
请以 JSON 格式输出：
\`\`\`json
{
  "titleTemplates": [
    {
      "pillar": "主支柱名称",
      "templates": [
        {
          "title": "具体标题",
          "type": "pain|number|counter_intuitive|emotional|story|warning|promise|contrast",
          "platform": "xiaohongshu|douyin|general",
          "reason": "为什么这个标题有效"
        }
      ]
    }
  ],
  "totalTemplates": 30,
  "confidence": 0.85
}
\`\`\`

## 质量要求
- 每个支柱至少10个标题
- 标题类型要多样化
- 标题要符合品牌调性：${context.brandTone || '专业、亲切、有温度'}
- 避免夸大和虚假宣传
- 符合广告法和平台规范

开始创作：`;
}
